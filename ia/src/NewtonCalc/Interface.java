    private boolean isNumeric(String input){
        try {
            Double.parseDouble(input);
            return true;
        } catch (NumberFormatException err) {
            return false;
        }
    }
    
    private boolean isInt(String input){
        try{
            Integer.parseInt(input);
            return true;
        } catch (NumberFormatException err) {
            return false;
        }
    }
    
